public class Car {
	private int carID;
	private String carLienceNumber;
	private Integer pricePerHour;
	private String carModel;
	private Integer basePrice;
	private String manufacturer;
	private Integer securityDeposit;
	/**
	 * @return the carID
	 */
	public int getCarID() {
		return carID;
	}
	/**
	 * @param carID the carID to set
	 */
	public void setCarID(int carID) {
		this.carID = carID;
	}
	/**
	 * @return the carLienceNumber
	 */
	public String getCarLienceNumber() {
		return carLienceNumber;
	}
	/**
	 * @param carLienceNumber the carLienceNumber to set
	 */
	public void setCarLienceNumber(String carLienceNumber) {
		this.carLienceNumber = carLienceNumber;
	}
	/**
	 * @return the pricePerHour
	 */
	public double getPricePerHour() {
		return pricePerHour;
	}
	/**
	 * @param pricePerHour the pricePerHour to set
	 */
	public void setPricePerHour(Integer pricePerHour) {
		this.pricePerHour = pricePerHour;
	}
	/**
	 * @return the carModel
	 */
	public String getCarModel() {
		return carModel;
	}
	/**
	 * @param carModel the carModel to set
	 */
	public void setCarModel(String carModel) {
		this.carModel = carModel;
	}
	/**
	 * @return the basePrice
	 */
	public double getBasePrice() {
		return basePrice;
	}
	/**
	 * @param basePrice the basePrice to set
	 */
	public void setBasePrice(Integer basePrice) {
		this.basePrice = basePrice;
	}
	/**
	 * @return the manufacturer
	 */
	public String getManufacturer() {
		return manufacturer;
	}
	/**
	 * @param manufacturer the manufacturer to set
	 */
	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}
	/**
	 * @return the securityDeposit
	 */
	public double getSecurityDeposit() {
		return securityDeposit;
	}
	/**
	 * @param securityDeposit the securityDeposit to set
	 */
	public void setSecurityDeposit(Integer securityDeposit) {
		this.securityDeposit = securityDeposit;
	}
	

}
